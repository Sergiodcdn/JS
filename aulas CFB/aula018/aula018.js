// WHILE e DO WHILE:
// WHILE: primeiro testa, depois faz bloco de comando;
// DO WHILE: primeiro faz bloco de comando, depois testa;

n = 10

while (n < 10){
    console.log("Te tem de ser bonzinho")
    n ++
}

console.log('-----Fim do Programa-----')
//
do{
    console.log("Te tem de ser bonzinho")
    n ++
}while (n < 10)

console.log('-----Fim do Programa-----')
